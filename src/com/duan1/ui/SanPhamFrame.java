    void fillComboBox() {
        DefaultComboBoxModel model = (DefaultComboBoxModel) cboDoanhMuc.getModel();
        model.removeAllElements();
        try {
            List<DoanhMuc> list = dmdao.select();
            for (DoanhMuc dm : list) {
                model.addElement(dm);
            }

            cboDoanhMuc.setRenderer(new DefaultListCellRenderer() {
                @Override
                public Component getListCellRendererComponent(JList<?> list, Object value, int index, boolean isSelected, boolean cellHasFocus) {
                    super.getListCellRendererComponent(list, value, index, isSelected, cellHasFocus);
                    if (value instanceof DoanhMuc) {
                        DoanhMuc doanhMuc = (DoanhMuc) value;
                        setText(doanhMuc.getTenDoanhMuc()); // Hiển thị tên danh mục
                    }
                    return this;
                }
            });

            cboDoanhMuc.setSelectedIndex(0);
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }

