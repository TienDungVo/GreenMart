    void load() {
        DefaultTableModel model = (DefaultTableModel) tblSanPham.getModel();
        model.setRowCount(0);
        try {
            List<SanPham> list = daosp.select();
            for (SanPham dm : list) {
                Object[] row = {
                    dm.getIDSanPham(),
                    dm.getIDDoanhMuc(),
                    dm.getGia(),
                    dm.getMoTa(),
                    dm.getHinhAnh(),
                    dm.getTenSanPham()
                };
                model.addRow(row);
            }
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }

    void insert() {
        SanPham model = getModel();

        try {
            daosp.insert(model);
            this.load();
            clear();
            DialogHelper.alert(this, "Thêm mới thành công!");
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }

    }

    void update() {
        SanPham model = getModel();
        try {
            daosp.update(model);
            this.load();
            clear();
            DialogHelper.alert(this, "Cập nhật thành công!");
        } catch (Exception e) {
            DialogHelper.alert(this, "Cập nhật thất bại!");
            System.out.println(e.getMessage());
        }
    }

    SanPham getModel() {
        SanPham model = new SanPham();
        DoanhMuc doanhMuc = (DoanhMuc) cboDoanhMuc.getSelectedItem();
        model.setIDSanPham(txtMaSP.getText());
        model.setIDDoanhMuc(doanhMuc.getIDDoanhMuc());
        model.setTenSanPham(txtTenSanPham.getText());
        model.setGia(Integer.parseInt(txtGia.getText()));
        model.setMoTa(txtMoTa.getText());
        model.setHinhAnh(lblHinhAnh.getToolTipText());

        return model;
    }

    void setModel(SanPham model) {
        txtMaSP.setText(String.valueOf(model.getIDSanPham()));
        txtTenSanPham.setText(model.getTenSanPham());
        txtMoTa.setText(model.getMoTa());
        txtGia.setText(String.valueOf(model.getGia()));

        DefaultComboBoxModel model1 = (DefaultComboBoxModel) cboDoanhMuc.getModel();
        for (int i = 0; i < model1.getSize(); i++) {
            DoanhMuc item = (DoanhMuc) model1.getElementAt(i);
            if (item.getIDDoanhMuc() == model.getIDDoanhMuc()) {
                cboDoanhMuc.setSelectedIndex(i);
            }
        }

        cboDoanhMuc.setSelectedItem(model.getIDDoanhMuc());

        if (model.getHinhAnh() != null) {
            lblHinhAnh.setIcon(XImage.read(model.getHinhAnh()));
            lblHinhAnh.setToolTipText(model.getHinhAnh());
        } else {
            lblHinhAnh.setText("");
            lblHinhAnh.setIcon(null);
            lblHinhAnh.setToolTipText(null);

        }
    }

    void ChonAnh() {
        if (fileChooser.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) {
            File file = fileChooser.getSelectedFile();
            XImage.save(file);
            ImageIcon icon = XImage.read(file.getName());
            lblHinhAnh.setIcon(icon);
            lblHinhAnh.setToolTipText(file.getName());
        }
    }

    void fillIdsangtable() {
        DoanhMuc dm1 = (DoanhMuc) cboDoanhMuc1.getSelectedItem();//lay duoc doi tuong danh muc

        DefaultTableModel model = (DefaultTableModel) tblSanPham.getModel();
        model.setRowCount(0);
        try {
            List<SanPham> list = daosp.findById(dm1.getIDDoanhMuc());
            for (SanPham dm : list) {
                Object[] row = {
                    dm.getIDSanPham(),
                    dm.getIDDoanhMuc(),
                    dm.getGia(),
                    dm.getMoTa(),
                    dm.getHinhAnh(),
                    dm.getTenSanPham()
                };
                model.addRow(row);
            }
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }

    void edit() {
        try {
            String masp = (String) tblSanPham.getValueAt(this.index, 0);
            SanPham model = daosp.findByIdSp(masp);
            if (model != null) {
                this.setModel(model);

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,"Vượt Quá Danh Sách");
        }
    }

    void delete() {
        if (DialogHelper.confirm(this, "Bạn có muốn xóa hay không?")) {

            String maSP = txtMaSP.getText();
            try {
                daosp.delete(maSP);
                this.load();
                clear();
                DialogHelper.alert(this, "Xóa thành công!");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Còn Sản Phẩm Trong Kho , Hãy Đảm Bảo Sản Phẩm Đã Hết Tránh Viếc Sảy Ra Lỗi !!");
            }

        }
    }

    void clear() {
        this.setModel(new SanPham());
    }

