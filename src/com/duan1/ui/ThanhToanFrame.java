    void TongTien() {
        try {
            int tong = 0;
            DefaultTableModel model = (DefaultTableModel) tblgiohang.getModel();
            int rowCount = model.getRowCount();

            for (int i = 0; i < rowCount; i++) {
                int col2Value = (int) model.getValueAt(i, 2);
                int col3Value = (int) model.getValueAt(i, 3);
                int product = col2Value * col3Value;
                tong += product;
            }

            setTien(tong);
        } catch (Exception e) {
            System.out.println("lỗi hàm tổng tiền ");
        }
    }

    void TinhTien() {
        String strTienKhach = txtKhachDua.getText();
        String strTongTien = txtTongTien.getText();
        String strVoCher = txtGiaTri.getText();
        boolean check = true;
        try {
            int tienKhach = Integer.parseInt(strTienKhach);
            int tongTien = Integer.parseInt(strTongTien);

            if (tienKhach < tongTien || tienKhach <= 0) {
                JOptionPane.showMessageDialog(this, "Tiền khách đưa Thiếu hoặc nhỏ hơn 1");
                check = false;
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Tiền Phải là số");
            check = false;
        }
        if (strVoCher.isEmpty()) {
            strVoCher = "0";  // sử dụng giá trị mặc định khi strVoCher không có giá trị nhập vào
        }

        if (check) {
            int tienKhach = Integer.parseInt(strTienKhach);
            int tongTien = Integer.parseInt(strTongTien);
            int giamgia = Integer.parseInt(strVoCher);
            int tienThoi = tienKhach - tongTien - giamgia;
            txtTienTra.setText(Integer.toString(tienThoi));
        }

    }

