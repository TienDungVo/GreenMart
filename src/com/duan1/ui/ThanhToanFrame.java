    void fillidSptogiohang(String masp) {
        boolean check = true;
        DefaultTableModel model = (DefaultTableModel) tblgiohang.getModel();
        int rowCount = model.getRowCount();

        for (int i = 0; i < rowCount; i++) {
            String value = (String) model.getValueAt(i, 0);
            if (value.equals(masp)) {
                check = false;
                int quantity = (int) model.getValueAt(i, 2);
                model.setValueAt(quantity + 1, i, 2);
                break;
            }
        }

        if (check) {

            try {
                GioHang gh = gioHangDao.select(masp);

//                tblgiohang.getColumnModel().getColumn(4).setCellRenderer(new ButtonRenderer());
//                tblgiohang.getColumnModel().getColumn(4).setCellEditor(new ButtonEditor(new JCheckBox(), tblgiohang,tblgiohang.getSelectedRow()));
                Object[] row = {
                    gh.getIDSanPham(),
                    gh.getTenSanPham(),
                    1,
                    gh.getGia(), //                    "Xóa"
                };
                model.addRow(row);

            } catch (Exception e) {
                System.out.println(e.getMessage());
            }
        }

        TongTien();

    }

