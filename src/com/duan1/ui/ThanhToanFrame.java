    void bangGioHang() {
        TableActionEven even = new TableActionEven() {
            @Override
            public void onEdit(int row) {
                int rows = tblgiohang.getSelectedRow();
                System.out.println(rows);
                DefaultTableModel model = (DefaultTableModel) tblgiohang.getModel();

                // Kiểm tra xem có hàng được chọn hay không
                if (rows >= 0) {
                    try {
                        int quantity = (int) model.getValueAt(rows, 2);
                        if (quantity > 1) {
                            model.setValueAt(quantity - 1, rows, 2);  // Subtract 1 from the quantity
                        } else {
                            model.setValueAt(1, rows, 2);  // Remove the row if quantity is 1 or less
                        }
                    } catch (ArrayIndexOutOfBoundsException e) {
                        System.out.println("lỏ" + e.getMessage());
                    }
                }

                TongTien();
            }

            @Override
            public void onDelete(int row) {
                if (tblgiohang.isEditing()) {
                    tblgiohang.getCellEditor().stopCellEditing();
                }
                DefaultTableModel model = (DefaultTableModel) tblgiohang.getModel();
                model.removeRow(row);
                TongTien();
            }
        };
        try {
            tblgiohang.getColumnModel().getColumn(4).setCellRenderer(new TableActionCellRender());
            tblgiohang.getColumnModel().getColumn(4).setCellEditor(new TableActionCellEditor(even));

        } catch (Exception e) {
            System.out.println(e.getMessage()
            );
        }
    }

