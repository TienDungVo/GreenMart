    void duyetModel() {
        DefaultTableModel model = (DefaultTableModel) tblTenspSl.getModel();
        int rowCount = model.getRowCount();

        for (int i = 0; i < rowCount; i++) {
            String value = (String) model.getValueAt(i, 0);
            // In giá trị cột 0 của hàng thứ i ra màn hình
            SanPham sp = sanPhamDao.findByIdSp(value);
            model.setValueAt(sp.getTenSanPham(), i, 0);
        }

    }

