    public void tinhVaHienThiTongDoanhThu(List<GioHang> list) {
        int tongDoanhThu = 0;
        for (GioHang gioHang : list) {
            tongDoanhThu += gioHang.getSoLuong() * gioHang.getGia();
        }
        NumberFormat currencyFormat = NumberFormat.getCurrencyInstance(new Locale("vi", "VN"));
        txtTongDoanhThu.setText(currencyFormat.format(tongDoanhThu));

    }

    void ThongKETheoBieuDo() {
        DefaultCategoryDataset dataset = new DefaultCategoryDataset();
        String txtngay = txtNgayBieuDo.getText();
        String[] parts = txtngay.split("-");

        int month = Integer.parseInt(parts[0]); // Ép kiểu month sang int
        int year = Integer.parseInt(parts[1]);

        List<ThongKeBieuDo> list = tkBddao.SelectbyTime(month, year);
        int tongtien = 0;
        for (ThongKeBieuDo thongKeBieuDo : list) {
            tongtien += thongKeBieuDo.getDoanhThu();
            dataset.setValue(thongKeBieuDo.getDoanhThu(), thongKeBieuDo.getTenDanhMuc(), thongKeBieuDo.getTenDanhMuc());
        }
        dataset.addValue(tongtien, "Tổng Tiền", "Tổng Tiền ");
        // Tạo biểu đồ cột
        JFreeChart chart;
        chart = ChartFactory.createBarChart(
                "Biểu Đồ Thống Kê Doanh Thu",
                "Doanh Mục",
                "Doanh Thu (VND)",
                dataset,
                PlotOrientation.VERTICAL,
                true,
                true,
                false
        );

        // Hiển thị biểu đồ lên JPanel
        ChartPanel chartPanel = new ChartPanel(chart);
        panelBieuDoThongKeDoanhMuc.setLayout(new java.awt.BorderLayout());
        panelBieuDoThongKeDoanhMuc.add(chartPanel, java.awt.BorderLayout.CENTER);
        panelBieuDoThongKeDoanhMuc.validate();
    }

