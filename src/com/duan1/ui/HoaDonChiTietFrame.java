    void fillComboBox() {
        DefaultComboBoxModel model = (DefaultComboBoxModel) cboNhanVien.getModel();
        model.removeAllElements();
        try {
            List<TaiKhoan> list = nvdoa.select();
            for (TaiKhoan tk : list) {
                model.addElement((TaiKhoan) tk);
            }
            cboNhanVien.setRenderer(new DefaultListCellRenderer() {
                @Override
                public Component getListCellRendererComponent(JList<?> list, Object value, int index, boolean isSelected, boolean cellHasFocus) {
                    super.getListCellRendererComponent(list, value, index, isSelected, cellHasFocus);
                    if (value instanceof TaiKhoan) {
                        TaiKhoan tk = (TaiKhoan) value;
                        setText((String) tk.getUserName()); // Hiển thị tên danh mục
                    }
                    return this;
                }
            });

            cboNhanVien.setSelectedIndex(0);

        } catch (Exception e) {

            System.out.println(e.getMessage());
        }
    }

